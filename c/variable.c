#include <stdio.h>

int main() {
    // Integer variable
    int myInt = 42;
    printf("Integer value: %d\n", myInt);
    
    // Float variable
    float myFloat = 3.14;
    printf("Float value: %f\n", myFloat);
    
    // Character variable
    char myChar = 'A';
    printf("Character value: %c\n", myChar);
    
    return 0;
}

